function t(key) {
  const lang = localStorage.getItem('tp_lang') || 'ru';
  return (I18N[lang] && I18N[lang][key]) ? I18N[lang][key] : (I18N['en'][key] || key);
}

function setLang(lang) {
  localStorage.setItem('tp_lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  if (typeof renderCalcStep === 'function') renderCalcStep();
  if (typeof renderProducts === 'function') renderProducts();
  if (typeof renderCart === 'function') renderCart();
  if (typeof renderFavorites === 'function') renderFavorites();
  if (typeof renderContacts === 'function') renderContacts();
}

function getCurrentLang() {
  return localStorage.getItem('tp_lang') || 'ru';
}
